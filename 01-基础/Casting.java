public class Casting {
    public static void main(String[] args) {
        int a = 11;
        double b = a;

        System.out.println(a);
        System.out.println(b);
        
        double c = 11.11;
        int d = (int) c;

        System.out.println(c);
        System.out.println(d);
    }
}
