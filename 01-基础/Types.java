public class Types {
    public static void main(String[] args) {
        String str = "str";
        System.out.println(str);
        char single = 'a';
        System.out.println(single);

        int n1 = 1;
        System.out.println(n1);

        float n2 = 13.1111f;
        System.out.println(n2);
       
        boolean a = true;
        System.out.println(a);
    }
}
